public class Colores {
    public static String verde() {
        return "\u001B[32m";
    }

    public static String amarillo() {
        return "\u001b[33m";
    }

    public static String rojo() {
        return "\u001b[31m";
    }

    public static String blanco() {
        return "\u001B[0m";
    }

    public static String violeta() {
        return "\u001B[35m";
    }
}
